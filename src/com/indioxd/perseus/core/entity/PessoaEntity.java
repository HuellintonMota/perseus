    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final PessoaEntity other = (PessoaEntity) obj;
        return Objects.equals(this.codigo, other.codigo);
    }
    
    

