    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        PessoaEntity f = new PessoaEntity();
        ClienteService fs = new ClienteService();
        //tipo
        if(cboTipo.getSelectedIndex()==0){
            f.setTipo("J");
        }else{
            f.setTipo("F");
        }
        //data cadastro
        //Auto
        //Razão Social
        f.setRazaoSocial(txtRazaoSocial.getText());
        //fantasia
        f.setFantasia(txtFantasia.getText());
        //cnpj
        f.setCpfCnpj(txtCnpj.getText());
        //IE
        f.setInscricaoEstadual(txtInscricao.getText());
        //Data nascto
        if(!txtNascto.getText().equals("")&&txtNascto.getText()!=null){
            try {
                f.setDataNascto(new SimpleDateFormat("dd/MM/yyyy").parse(txtNascto.getText()));
            } catch (ParseException ex) {
                Logger.getLogger(TelaCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        //Endereco
        //logradouro
        f.setEnderecoLogradouro(txtLogradouro.getText());
        //bairro
        f.setEnderecoBairro(txtBairro.getText());
        //numero
        f.setEnderecoNumero(txtNumero.getText());
        //complemento
        f.setEnderecoComplemento(txtComplemento.getText());
        //cep
        f.setEnderecoCEP(txtCEP.getText());
        //cidade
        f.setEnderecoCidade(txtCidade.getText());
        //UF
        f.setEnderecoUF(cboUF.getSelectedItem().toString());
        
        if(txtID.getText().equals("")){

            try {
                JOptionPane.showMessageDialog(rootPane, fs.salvarCliente(f));
               
            } catch (NegocioException ex) {
                Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
                ex.ExibeErro();
            }
        }
        else{
            f.setCodigo(cliente.getCodigo());
            try {
            JOptionPane.showMessageDialog(rootPane, fs.atualizaCliente(f));
            } catch (NegocioException ex) {
            
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            ex.ExibeErro();
            }
            
            
        }
            limpar();
    }//GEN-LAST:event_btnSalvarActionPerformed

