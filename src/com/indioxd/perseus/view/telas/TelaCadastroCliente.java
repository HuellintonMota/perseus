    private void carregaCliente(){
           
        PessoaEntity f = new PessoaEntity();
        ClienteService fs = new ClienteService();
        
        //tipo
        if(f.getTipo().equals("J")){
            cboTipo.setSelectedIndex(0);
            setPessoaJuridica();
        }else{
            cboTipo.setSelectedIndex(1);
            setPessoaFisica();
        }
        txtID.setText(f.getCodigo().toString());
        //data cadastro
        txtCadastro.setText(f.getDataCadastro().toString());
        //Razão Social
        txtRazaoSocial.setText(f.getRazaoSocial());
        //fantasia
        txtFantasia.setText(f.getFantasia());
        //cnpj
        txtCnpj.setValue(f.getCpfCnpj());
        //IE
        txtInscricao.setText(f.getInscricaoEstadual());
        //Data nascto
        txtNascto.setValue(f.getDataNascto());
        //Endereco
        //logradouro
        txtLogradouro.setText(f.getEnderecoLogradouro());
        //bairro
        txtBairro.setText(f.getEnderecoBairro());
        //numero
        txtNumero.setText(f.getEnderecoNumero());
        //complemento
        txtComplemento.setText(f.getEnderecoComplemento());
        //cep
        txtCEP.setText(f.getEnderecoCEP());
        //cidade
        txtCidade.setText(f.getEnderecoCidade());
        //UF
        cboUF.setSelectedItem(f.getEnderecoUF());
    }
   
