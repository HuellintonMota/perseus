    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        // TODO add your handling code here:
        
        VendaEntity vendaFiltro = new VendaEntity();
        
        if(!txtData.getText().equals("")&&txtData.getText()!=null){
            try {
                vendaFiltro.setDataVenda(new SimpleDateFormat("dd/MM/yyyy").parse(txtData.getText()));
            } catch (ParseException ex) {
                Logger.getLogger(TelaCadastroCliente.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
                
        Long codigo = null;
        if(!txtFiltroCodigo.getText().equals("")){
            codigo = Long.parseLong(txtFiltroCodigo.getText());
        }
        
        vendaFiltro.setCodigo(codigo);
        vendaFiltro.setCliente(cliente);
        listarProdutosFiltrados(vendaFiltro);
    }//GEN-LAST:event_btnFiltrarActionPerformed

    private void txtIdClienteFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtIdClienteFocusLost
        try {
            // TODO add your handling code here:
            if(!txtIdCliente.getText().equals("")){
                PessoaEntity cli = new ClienteService().buscarClientePorID(Long.parseLong(txtIdCliente.getText()));
                if(cli != null){
                    txtNomeCliente.setText(cli.getFantasia()+" - "+cli.getCpfCnpj());
                }else{
                    txtNomeCliente.setText("");
                }
                    this.cliente = cli;
            }
        } catch (NegocioException ex) {
            Logger.getLogger(TelaNovaVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtIdClienteFocusLost

