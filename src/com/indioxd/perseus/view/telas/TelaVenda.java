   private void listarVendas(){
        try {
            vendas = new VendaService().listaVendas();
            DefaultTableModel model = (DefaultTableModel) tblVendas.getModel();
            model.getDataVector().removeAllElements();
            for (VendaEntity pro : vendas) {
                model.addRow(new Object[]{pro.getCodigo(),df.format(pro.getDataVenda()), pro.getCliente().getCodigo()+" - "+pro.getCliente().getFantasia(), cf.format(pro.getTotal())});
            }
        } catch (NegocioException ex) {
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            ex.ExibeErro();
        }
        
    }
   private void listarProdutosFiltrados(VendaEntity v){
        try {
            vendas = new VendaService().listaVendaFiltrada(v);
            DefaultTableModel model = (DefaultTableModel) tblVendas.getModel();
            model.getDataVector().removeAllElements();
            for (VendaEntity pro : vendas) {
                model.addRow(new Object[]{pro.getCodigo(),df.format(pro.getDataVenda()), pro.getCliente().getCodigo()+" - "+pro.getCliente().getFantasia(), cf.format(pro.getTotal())});
            }
        } catch (NegocioException ex) {
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            ex.ExibeErro();
        } catch (NullPointerException ne){
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ne);
        }
        
    }
    
