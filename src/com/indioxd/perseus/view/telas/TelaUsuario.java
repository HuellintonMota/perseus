   private void listarUsuarios(){
        try {
            usuarios = new UsuarioService().listarUsuario();
            DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
            model.getDataVector().removeAllElements();
            for (UsuarioEntity usuario : usuarios) {
                model.addRow(new Object[]{usuario.getCodigo(),usuario.getNome(), usuario.getLogin(),usuario.getSenha(),usuario.getEmail()});
            }
        } catch (NegocioException ex) {
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            ex.ExibeErro();
        }
        
    }
   
   private void listarUsuariosFiltrados(UsuarioEntity usuario){
        try {
            usuarios = new UsuarioService().listarUsuarioFiltrado(usuario);
            DefaultTableModel model = (DefaultTableModel) tblUsuarios.getModel();
            model.getDataVector().removeAllElements();
            for (UsuarioEntity usu : usuarios) {
                model.addRow(new Object[]{usu.getCodigo(),usu.getNome(), usu.getLogin(),usu.getSenha(),usu.getEmail()});
            }
        } catch (NegocioException ex) {
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            ex.ExibeErro();
        }
        
    }
    
