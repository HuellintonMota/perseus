    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        // TODO add your handling code here:
        UsuarioEntity usuarioFiltro = new UsuarioEntity();
        usuarioFiltro.setNome(txtFiltroNome.getText());
        usuarioFiltro.setLogin(txtFiltroLogin.getText());
        usuarioFiltro.setEmail(txtFiltroEmail.getText());
        
        Long codigo = null;
        if(!txtFiltroCodigo.getText().equals("")){
            codigo = Long.parseLong(txtFiltroCodigo.getText());
        }
        usuarioFiltro.setCodigo(codigo);
        
        listarUsuariosFiltrados(usuarioFiltro);
        
    }//GEN-LAST:event_btnFiltrarActionPerformed

