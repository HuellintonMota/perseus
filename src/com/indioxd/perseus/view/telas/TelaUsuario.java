    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        UsuarioEntity usuarioSelecionado = usuarios.get(tblUsuarios.getSelectedRow());
        if(JOptionPane.showConfirmDialog(rootPane, "Confirmar exclusão do usuário "+usuarioSelecionado.getNome()+"?", "Excluir Usuário", JOptionPane.YES_NO_OPTION)==0){
            try {
                new UsuarioService().excluirUsuario(usuarioSelecionado.getCodigo());
            } catch (NegocioException ex) {
                Logger.getLogger(TelaUsuario.class.getName()).log(Level.SEVERE, null, ex);
                ex.ExibeErro();
            }
            
            //listarUsuarios();
            limpar();
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

