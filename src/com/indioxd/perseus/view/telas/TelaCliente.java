    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        // TODO add your handling code here:
        
        PessoaEntity fornFiltro = new PessoaEntity();
        fornFiltro.setRazaoSocial(txtFiltroRazao.getText());
        fornFiltro.setFantasia(txtFiltroFantasia.getText());
        fornFiltro.setCpfCnpj(txtFiltroCNPJ.getText());
        
        Long codigo = null;
        if(!txtFiltroCodigo.getText().equals("")){
            codigo = Long.parseLong(txtFiltroCodigo.getText());
        }
        
        fornFiltro.setCodigo(codigo);
        listarClientesFiltrados(fornFiltro);
    }//GEN-LAST:event_btnFiltrarActionPerformed

