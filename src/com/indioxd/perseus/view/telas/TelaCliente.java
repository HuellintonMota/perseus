    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        PessoaEntity forn = clientes.get(tblClientes.getSelectedRow());
        if(JOptionPane.showConfirmDialog(rootPane, "Confirmar exclus찾o do usu찼rio "+forn.getRazaoSocial()+"?", "Excluir Usu찼rio", JOptionPane.YES_NO_OPTION)==0){
            try {
                new ClienteService().excluirCliente(forn.getCodigo());
            } catch (NegocioException ex) {
                Logger.getLogger(TelaCliente.class.getName()).log(Level.SEVERE, null, ex);
                ex.ExibeErro();
            }
            
            listarClientes();
            limpar();
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

