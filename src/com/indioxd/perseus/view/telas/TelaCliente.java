   private void listarClientes(){
        try {
            clientes = new ClienteService().listarCliente();
            DefaultTableModel model = (DefaultTableModel) tblClientes.getModel();
            model.getDataVector().removeAllElements();
            for (PessoaEntity forn : clientes) {
                model.addRow(new Object[]{forn.getCodigo(),forn.getRazaoSocial(), forn.getFantasia(), forn.getCpfCnpj(),forn.getDataCadastro()});
            }
        } catch (NegocioException ex) {
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            ex.ExibeErro();
        }
        
    }
   private void listarClientesFiltrados(PessoaEntity cliente){
        try {
            clientes = new ClienteService().listarClienteFiltrado(cliente);
            DefaultTableModel model = (DefaultTableModel) tblClientes.getModel();
            model.getDataVector().removeAllElements();
            for (PessoaEntity forn : clientes) {
                model.addRow(new Object[]{forn.getCodigo(),forn.getRazaoSocial(), forn.getFantasia(), forn.getCpfCnpj(),forn.getDataCadastro()});
            }
        } catch (NegocioException ex) {
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            ex.ExibeErro();
        }
        
    }
    
