    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        ProdutoEntity pro = produtos.get(tblProdutos.getSelectedRow());
        if(JOptionPane.showConfirmDialog(rootPane, "Confirmar exclusão do produto "+pro.getDescricao()+"?", "Excluir Produto", JOptionPane.YES_NO_OPTION)==0){
            try {
                new ProdutoService().excluirProduto(pro.getCodigo());
            } catch (NegocioException ex) {
                Logger.getLogger(TelaProduto.class.getName()).log(Level.SEVERE, null, ex);
                ex.ExibeErro();
            }
            
            listarProdutos();
            limpar();
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

