   private void listarProdutos(){
        try {
            produtos = new ProdutoService().listarProdutos();
            DefaultTableModel model = (DefaultTableModel) tblProdutos.getModel();
            model.getDataVector().removeAllElements();
            for (ProdutoEntity pro : produtos) {
                model.addRow(new Object[]{pro.getCodigo(),pro.getDescricao(), cf.format(pro.getPrecoVenda()), cf.format(pro.getPrecoCusto()),nf.format(pro.getEstoque())});
            }
        } catch (NegocioException ex) {
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            ex.ExibeErro();
        }
        
    }
   private void listarProdutosFiltrados(ProdutoEntity p){
        try {
            produtos = new ProdutoService().listarProdutoFiltrado(p);
            DefaultTableModel model = (DefaultTableModel) tblProdutos.getModel();
            model.getDataVector().removeAllElements();
            for (ProdutoEntity pro : produtos) {
                model.addRow(new Object[]{pro.getCodigo(),pro.getDescricao(), cf.format(pro.getPrecoVenda()), cf.format(pro.getPrecoCusto()),nf.format(pro.getEstoque())});
            }
        } catch (NegocioException ex) {
            Logger.getLogger(TelaCadastroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            ex.ExibeErro();
        }
        
    }
    
